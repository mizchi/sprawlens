/**
 * Symbol classification glyphs, drawn when zoomed into the symbol layer.
 * Shapes are VS Code Codicons (the `symbol-*` set, MIT/CC-BY) so they read
 * like an editor's outline; "component" is a custom JSX-style chevron for the
 * CamelCase function/value heuristic, which Codicons has no icon for.
 */
export type SymbolGlyph =
  | "function"
  | "component"
  | "class"
  | "variable"
  | "type"
  | "interface"
  | "enum";

/** Map a producer kind + name to a glyph. CamelCase functions/values read as
 * components (React etc.); everything else keeps its declaration glyph. */
export function symbolGlyphOf(
  kind: SymbolKind | undefined,
  name: string,
): SymbolGlyph | null {
  if (!kind) return null;
  const camel = /^[A-Z]/.test(name);
  if ((kind === "function" || kind === "variable") && camel) return "component";
  return kind;
}

